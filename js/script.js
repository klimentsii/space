let bg = document.querySelector('.body__bg');
window.addEventListener('mousemove', function (e) {
   let x = e.clientX / window.innerWidth;
   let y = e.clientY / window.innerHeight;
   bg.style.transform = 'translate(-' + x * 100 + 'px, -' + y * 100 + 'px)';
});
